<?php
require_once 'config/database.php';
require_once 'includes/auth.php';

// Redirect if already logged in
if (isLoggedIn()) {
    header('Location: /index.php');
    exit();
}

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = sanitize($_POST['username'] ?? '');
    $password = $_POST['password'] ?? '';
    
    if (empty($username) || empty($password)) {
        $error = 'Please enter both username and password.';
    } else {
        $db = getDB();
        $stmt = $db->prepare("SELECT admin_id, username, password_hash, role, full_name, is_active FROM admins WHERE username = ? AND is_active = 1");
        $stmt->execute([$username]);
        $admin = $stmt->fetch();
        
        if ($admin && password_verify($password, $admin['password_hash'])) {
            // Update last login
            $updateStmt = $db->prepare("UPDATE admins SET last_login = NOW() WHERE admin_id = ?");
            $updateStmt->execute([$admin['admin_id']]);
            
            loginUser($admin['admin_id'], $admin['username'], $admin['role']);
            header('Location: /index.php');
            exit();
        } else {
            $error = 'Invalid username or password.';
        }
    }
}
